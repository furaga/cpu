#define SIGN(i) ((0x80000000&(i))>>31)
#define EXPO(i) ((0x7f800000&(i))>>23)
#define FRAC(i) (0x007fffff&(i))

extern uint64_t fsqrt_table[];
uint32_t _fsqrt(uint32_t i1) {
	uint32_t a1,key;
	uint32_t so,eo,fo,ret;
	uint32_t const23,grad13;
	uint64_t v;
	key = (i1>>14) & 0x3ff;
	v = fsqrt_table[key];
	const23 = (v >> 13)&0x7fffff;				// 23bit
	grad13 = v & 0x1fff;					// 13bit
	a1  = FRAC(i1) & 0x3fff;				// 14bit
	fo = const23 + ((a1 * grad13) >> 13);
	so = 0;
	so <<= 31;
	eo = (127 + (EXPO(i1))) / 2;
	eo <<= 23;
	ret = so|eo|fo;

	return ret;
}

