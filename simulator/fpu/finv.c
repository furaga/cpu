#define SIGN(i) ((0x80000000&(i))>>31)
#define EXPO(i) ((0x7f800000&(i))>>23)
#define FRAC(i) (0x007fffff&(i))

extern uint64_t finv_table[];
uint32_t _finv(uint32_t i1) {
	uint32_t a1,key;
	uint32_t so,eo,fo,ret;
	uint32_t const23,grad13;
	uint64_t v;

	key = (i1>>13) & 0x3ff;				// 10bit
	v = finv_table[key];
	const23 = v >> 13;
	grad13 = v & 0x1fff;					// 13bit
	a1  = i1 & 0x1fff;				// 13bit

	fo = (FRAC(i1) == 0) ? 0:const23 - ((a1 * grad13) >> 12);
	so = SIGN(i1);
	so <<= 31;

	switch (EXPO(i1)) {
		case 255: eo = 255; break;
		case 254: eo = 0; break;
		case  0 : eo = 255; break;
		default:
			if (FRAC(i1) == 0) {
				eo = 254 - EXPO(i1);
			} else {
				eo = 253 - EXPO(i1);
			}
		break;
	}

	eo <<= 23;
	ret = so|eo|fo;

	return ret;
}

